package carser;


    public class Carr {
        private int id;
        private String model;
        private String color;


        public int getId() {
            return id;
        }

        public Carr setId(int id) {
            this.id = id;
            return this;
        }

        public String getModel() {
            return model;
        }

        public Carr setModel(String model) {
            this.model = model;
            return this;
        }

        public String getColor() {
            return color;
        }

        public Carr setColor(String color) {
            this.color = color;
            return this;
        }
    }

